# Change data type (Dynamic Typing

x = 10
print(x)
print(type(x))

x = "Python"
print(x)
print(type(x))

# Note Ek hi variable m alag alag type k data store kar sakte hai

a = b = c = 100

print(a)
print(b)
print(c)

name, age, city = "Raja", 22, "Delhi"
print(name)
print(age)
print(city)

# swap Variables

a = 10 
b = 20
print("Before:", a, b)

a, b = b, a

print("After:", a, b)

name = "Saif Farid"

print(type(name))
print(id(name))

# Value update

score = 50
print(score)

score = 80
print(score)  # variable same rate hai but value chnage ho jate hai

